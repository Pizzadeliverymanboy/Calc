package com.example.calc;

public class calc1 {
	
	protected static int add(int a, int b) {
		return a + b;
	}
	
	protected static int sub(int a, int b) {
		return a - b;
	}
	protected static int mul(int a, int b) {
		return a * b;
	}
	protected static int div(int a, int b) {
		return a / b;
	}



}
